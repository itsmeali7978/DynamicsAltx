// Global translation functions
window.translateElement = function(el) {
  const key = el.getAttribute('data-i18n');
  const lang = localStorage.getItem('lang') || 'en';
  if (window.i18n[lang] && window.i18n[lang][key]) {
    if (el.tagName === 'INPUT') {
      if (el.placeholder || el.getAttribute('placeholder') !== null) {
        el.placeholder = window.i18n[lang][key];
      }
    } else {
      el.textContent = window.i18n[lang][key];
    }
  }
};

window.applyTranslations = function() {
  document.querySelectorAll('[data-i18n]').forEach(window.translateElement);
};

window.setLanguage = function(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.setAttribute('lang', lang);
  
  if (lang === 'ar') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.body.classList.add('rtl-layout');
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.body.classList.remove('rtl-layout');
  }
  
  window.applyTranslations();
  document.dispatchEvent(new CustomEvent('languageChanged', { detail: lang }));
};
